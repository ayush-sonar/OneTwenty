from fastapi import FastAPI
from fastapi.middleware.cors import CORSMiddleware
from app.core.config import settings
from app.api.v1.api import api_router
from app.db.mongo import db
from app.middleware.logging import LoggingMiddleware

app = FastAPI(
    title=settings.PROJECT_NAME,
    openapi_url=f"{settings.API_V1_STR}/openapi.json"
)

# Configure CORS
app.add_middleware(
    CORSMiddleware,
    allow_origins=["*"],
    allow_credentials=True,
    allow_methods=["*"],
    allow_headers=["*"],
)

# Add logging middleware
app.add_middleware(LoggingMiddleware)

@app.on_event("startup")
async def startup_db_client():
    db.connect()
    # Ensure MongoDB indexes exist (idempotent â€” safe to run on every boot)
    from app.repositories.entries import EntriesRepository
    await EntriesRepository().ensure_indexes()


@app.on_event("shutdown")
async def shutdown_db_client():
    db.close()

@app.get("/")
def root():
    return {"message": "Welcome to Nightscout SaaS API"}

app.include_router(api_router, prefix=settings.API_V1_STR)
